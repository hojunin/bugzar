// Generates packages/sdk/src/viewer-asset.generated.ts — the @bugzar/viewer
// single-file IIFE (React + rrweb + the whole viewer) embedded as a string so
// the SDK's `@bugzar/sdk/export` can inline it into a self-contained, offline
// `file://` replay HTML.
//
// Mirrors scripts/gen-rrweb-player.mjs. Run after building the inline bundle:
//   pnpm --filter @bugzar/viewer build:inline && node scripts/gen-viewer-asset.mjs
// (the SDK `prebuild` does both).
import { existsSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(here, '..');

const src = join(repoRoot, 'packages/viewer/dist-inline/bugzar-viewer-inline.js');
if (!existsSync(src)) {
  throw new Error(`viewer inline bundle not found at ${src} — run "pnpm --filter @bugzar/viewer build:inline" first`);
}
const js = readFileSync(src, 'utf8');

const out = `// GENERATED by scripts/gen-viewer-asset.mjs — do not edit by hand.
// The @bugzar/viewer single-file IIFE (React + rrweb + viewer), embedded so the
// offline HTML export is self-contained. Regenerate:
//   pnpm --filter @bugzar/viewer build:inline && node scripts/gen-viewer-asset.mjs
export const VIEWER_JS = ${JSON.stringify(js)};
`;

const target = join(repoRoot, 'packages/sdk/src/viewer-asset.generated.ts');
writeFileSync(target, out);
console.log(`gen-viewer-asset: wrote ${target} (js ${(js.length / 1024) | 0}KB)`);
