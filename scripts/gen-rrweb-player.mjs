// Generates packages/backend/src/rrweb-player-asset.generated.ts — the rrweb-player
// UMD + CSS embedded as strings so the Worker can serve them same-origin at
// /assets/rrweb-player.{js,css}. The SDK's slim replay viewer loads them from
// there, which satisfies the /r/:id CSP (`script-src 'self'`) without a CDN.
//
// Run via: pnpm --filter @bugzar/backend gen:player (auto-runs before dev/deploy/test).
import { existsSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(here, '..');

// rrweb-player's `exports` map blocks `require.resolve('rrweb-player/package.json')`,
// so resolve the dist dir directly from the workspace package symlink instead.
const distDir = [
  join(repoRoot, 'packages/backend/node_modules/rrweb-player/dist'),
].find((d) => existsSync(join(d, 'rrweb-player.umd.min.cjs')));
if (!distDir) throw new Error('rrweb-player dist not found — run pnpm install');
const version = JSON.parse(readFileSync(join(distDir, '..', 'package.json'), 'utf8')).version;

const js = readFileSync(join(distDir, 'rrweb-player.umd.min.cjs'), 'utf8');
const css = readFileSync(join(distDir, 'style.min.css'), 'utf8');

const out = `// GENERATED by scripts/gen-rrweb-player.mjs — do not edit by hand.
// rrweb-player ${version} UMD + CSS, served same-origin from the
// Worker (/assets/rrweb-player.{js,css}) so the slim SDK replay viewer can load
// it under the /r/:id CSP. Regenerate: pnpm --filter @bugzar/backend gen:player
export const RRWEB_PLAYER_JS = ${JSON.stringify(js)};
export const RRWEB_PLAYER_CSS = ${JSON.stringify(css)};
`;

const target = join(repoRoot, 'packages/backend/src/rrweb-player-asset.generated.ts');
writeFileSync(target, out);
console.log(`gen-rrweb-player: wrote ${target} (js ${(js.length / 1024) | 0}KB, css ${(css.length / 1024) | 0}KB)`);
